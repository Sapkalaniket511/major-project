package com.netspam.bean;

public class Features {
	private double burstiness;
	private double etf;
	private double negRatio;
	private double avgSim;
	private double maxSim;
	private double pp1;
	private double deviation;
	private double res;
	
	public double getBurstiness() {
		return burstiness;
	}
	public void setBurstiness(double burstiness) {
		this.burstiness = burstiness;
	}
	public double getEtf() {
		return etf;
	}
	public void setEtf(double etf) {
		this.etf = etf;
	}
	public double getNegRatio() {
		return negRatio;
	}
	public void setNegRatio(double negRatio) {
		this.negRatio = negRatio;
	}
	public double getAvgSim() {
		return avgSim;
	}
	public void setAvgSim(double avgSim) {
		this.avgSim = avgSim;
	}
	public double getMaxSim() {
		return maxSim;
	}
	public void setMaxSim(double maxSim) {
		this.maxSim = maxSim;
	}
	public double getPp1() {
		return pp1;
	}
	public void setPp1(double pp1) {
		this.pp1 = pp1;
	}
	public double getDeviation() {
		return deviation;
	}
	public void setDeviation(double deviation) {
		this.deviation = deviation;
	}
	public double getRes() {
		return res;
	}
	public void setRes(double res) {
		this.res = res;
	}
	

}
